/* JavaScript assignment operator is equal(=) which assigns the value of the right - hand operand to
its left - hand operand.That is if a = b assigns the value of b to a. */

// The simple assignment operator is used to assign a value to a variable.

let a = 10;
console.log(a);

let b;
b = 15;
console.log(b);

a = b;  // b's value is assigned to a
console.log(a);

// This operator adds the value to the right operand to a variable and assigns the result to the variable.

let c = 15;
c += 5; // c = c + 5;
console.log(c);

// This operator subtracts the value of the right operand from a variable and assigns the result to the variable.

let d = 15;
d -= 5; // d = d - 5;
console.log(d);

// This operator multiplies a variable by the value of the right operand and assigns the result to the variable.

let e = 15;
e *= 5; // e = e * 5;
console.log(e);

// This operator divides a variable by the value of the right operand and assigns the result to the variable.

let f = 15;
f /= 5; // f = f / 5;
console.log(f);

// This operator divides a variable by the value of the right operand and assigns the remainder to the variable.

let g = 15;
g %= 2; // g = g % 2;
console.log(g);

// This operator raises the value of a variable to the power of the right operand.

let h = 15;
h **= 5; // h = h ** 5;
console.log(h);

