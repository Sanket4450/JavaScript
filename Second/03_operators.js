// JavaScript operators are symbols that are used to perform operations on operands.

let sum = 10 + 5; // Here, + is an operator & 10 and 5 are operands.

// Arithmetic operators are used to perform arithmetic operations on the operands.

console.log(5 + 2); // addition

console.log(5 - 2); // subtraction

console.log(5 * 2); // multiplication

console.log(5 / 2); // division

console.log(5 % 2); // modulus

console.log(5 ** 2); // exponentiation

let a = 5;
console.log(a++); // increment

let b = 5;
console.log(b--); // decrement

let c = 5;
console.log(++c); // increment

let d = 5;
console.log(--d); // decrement

// Comparison operators compare the two operands.

console.log(5 == 2); // is equal to

console.log(5 === 2); // identical (equal and of same type)

console.log(5 != 2); // not equal to

console.log(5 !== 2); // not identical

console.log(5 > 2); // greater than

console.log(5 >= 2); // greater than or equal to

console.log(5 < 2); // less than

console.log(5 <= 2); // less than or equal to

